package zad2;

public class Cyclist {
    public String imie;
    public String nazwisko;
    public Integer bib;
    public float velo;

    public Cyclist(Integer bib, String imie, String nazwisko, float velo){
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.velo = velo;
        this.bib = bib;
    }
}
